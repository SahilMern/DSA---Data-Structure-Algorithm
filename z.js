function reverseString(inputString) {
  let reversedString = ""; // Initialize a new string to store the reversed string

  // Loop through the input string from the last character to the first
  for (let index = 0; index < inputString.length; index++) {
    reversedString = inputString[index] + reversedString; // Add the character to the beginning of reversedString
  }

  return reversedString; // Return the reversed string
}

console.log(reverseString("Hello")); // Output: "olleH"



